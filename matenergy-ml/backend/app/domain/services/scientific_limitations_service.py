class ScientificLimitationsService:
    """
    Returns curated scientific limitation warnings for display in reports
    and the UI.  All text is static — no generative AI.
    """

    def get_limitations_for_descriptor_type(
        self, descriptor_type: str
    ) -> list[str]:
        """
        Return limitations for the given descriptor type.

        Args:
            descriptor_type: "compositional", "structural", or "combined".
        """
        key = descriptor_type.lower()
        return _DESCRIPTOR_LIMITATIONS.get(key, [
            f"No specific limitations catalogued for descriptor type '{descriptor_type}'."
        ])

    def get_limitations_for_model_type(self, model_type: str) -> list[str]:
        """
        Return limitations for the given model type string.

        Args:
            model_type: One of the ModelType enum values (e.g. "random_forest_regressor").
        """
        key = model_type.lower().replace("-", "_")
        general = [
            "Machine learning models trained on DFT data inherit all DFT approximation "
            "errors. Predictions cannot be more accurate than the training labels."
        ]
        specific = _MODEL_LIMITATIONS.get(key, [
            f"No specific limitations catalogued for model type '{model_type}'."
        ])
        return general + specific

    def get_limitations_for_target_property(self, target_property: str) -> list[str]:
        """
        Return known limitations for a DFT target property.

        Args:
            target_property: e.g. "energy_above_hull", "band_gap".
        """
        return _PROPERTY_LIMITATIONS.get(target_property, [
            f"No specific limitations catalogued for property '{target_property}'."
        ])

    def get_general_prediction_limitations(self) -> list[str]:
        """Return general caveats that apply to all ML predictions in this system."""
        return _GENERAL_PREDICTION_LIMITATIONS

    def get_full_limitations_for_prediction(
        self,
        model_type: str,
        target_property: str,
        descriptor_type: str,
        is_out_of_domain: bool = False,
        is_uncalibrated: bool = True,
    ) -> list[str]:
        """
        Compose the full set of limitations relevant to a specific prediction.

        Args:
            model_type:       ML model used.
            target_property:  Property predicted.
            descriptor_type:  Descriptor set used for features.
            is_out_of_domain: Whether the prediction was flagged as OOD.
            is_uncalibrated:  Whether the model confidence is not calibrated.

        Returns:
            Deduplicated, ordered list of limitation strings.
        """
        warnings: list[str] = []
        warnings.extend(self.get_general_prediction_limitations())
        warnings.extend(self.get_limitations_for_model_type(model_type))
        warnings.extend(self.get_limitations_for_target_property(target_property))
        warnings.extend(self.get_limitations_for_descriptor_type(descriptor_type))

        if is_out_of_domain:
            warnings.insert(
                0,
                "WARNING: This material is outside the applicability domain of the "
                "training set. The prediction reliability is significantly reduced.",
            )
        if is_uncalibrated:
            warnings.append(
                "Confidence scores reported by this model have not been post-hoc "
                "calibrated. They should not be interpreted as probabilities."
            )

        # Deduplicate while preserving order
        seen: set[str] = set()
        unique: list[str] = []
        for w in warnings:
            if w not in seen:
                seen.add(w)
                unique.append(w)
        return unique
