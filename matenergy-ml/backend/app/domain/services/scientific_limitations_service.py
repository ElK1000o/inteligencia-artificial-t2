"""
ScientificLimitationsService
=============================
Provides static, rule-based scientific limitation warnings for:
  - descriptor types (compositional vs structural)
  - ML model types (regression, classification, neural network, etc.)
  - individual predictions (OOD, uncalibrated, extrapolation)
  - target properties (DFT accuracy, approximations)

All text is hard-coded, not generated by AI.  These warnings are intended
to appear in reports, UI tooltips, and prediction outputs to ensure
scientific honesty as required by the thesis scope.

Reference: docs/limitations.md
"""
